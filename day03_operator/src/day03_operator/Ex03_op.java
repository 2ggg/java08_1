package day03_operator;

public class Ex03_op {

	public static void main(String[] args) {
		/*
		 true / false
		 == : 같으면 true
		 != : 같지 않으면 true
		 */
		double a = 3.0, b = 3.1;
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		

	}

}
