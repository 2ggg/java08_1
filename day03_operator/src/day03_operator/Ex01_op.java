package day03_operator;

public class Ex01_op {

	public static void main(String[] args) {
		/*
		 /(나누기) : 정수 / 정수 = 정수(몫)
		 	-실수 / 정수 = 실수
		 %(mod) : 정수 % 정수 = 나머지
		 	10/3 = 3
		 	10/3.3 = 3.333...
		 	10%3 = 1
		 % 연산자를 이용해 어떤 수의 배수 또는 짝,홀 구분
		 10%2 = 0 짝수
		 10%2 = 1 홀수
		 n % m = 0 : m의 배수
		 */
		
		int a=9, b=2;
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		

	}

}
